const u8* find_glyph(char c)
{
    const char upper = static_cast<char>(std::toupper(static_cast<unsigned char>(c)));
    for (const Glyph& g : kFont) {
        if (g.ch == upper) {
            return g.col;
        }
    }
    return nullptr; // unknown -> space
}

void push_quad(std::vector<Vertex3D>& out, f32 x0, f32 y0, f32 x1, f32 y1, Vec3 color)
{
    const Vec3 n{0, 0, 1};
    const Vertex3D bl{{x0, y1, 0}, n, {0, 0}, color};
    const Vertex3D br{{x1, y1, 0}, n, {1, 0}, color};
    const Vertex3D tr{{x1, y0, 0}, n, {1, 1}, color};
    const Vertex3D tl{{x0, y0, 0}, n, {0, 1}, color};
    out.push_back(bl);
    out.push_back(br);
    out.push_back(tr);
    out.push_back(bl);
    out.push_back(tr);
    out.push_back(tl);
}

