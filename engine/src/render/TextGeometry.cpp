void build(std::string_view str, f32 x, f32 y, f32 size, f32 aspect, Vec3 color,
           std::vector<Vertex3D>& out)
{
    const f32 px_y = size / 7.0f;            // pixel height in NDC
    const f32 px_x = px_y / aspect;          // square pixels on screen
    f32 cursor = x;
    for (const char c : str) {
        const u8* cols = find_glyph(c);
        if (cols != nullptr) {
            for (int cx = 0; cx < 5; ++cx) {
                for (int cy = 0; cy < 7; ++cy) {
                    if ((cols[cx] >> cy & 1) != 0) {
                        const f32 qx = cursor + static_cast<f32>(cx) * px_x;
                        const f32 qy = y - static_cast<f32>(cy) * px_y;
                        push_quad(out, qx, qy, qx + px_x, qy - px_y, color);
                    }
                }
            }
        }
        cursor += 6.0f * px_x; // 5 glyph columns + 1 spacing column
    }
}

f32 measure(std::string_view str, f32 size, f32 aspect)
{
    const f32 px_x = (size / 7.0f) / aspect;
    return static_cast<f32>(str.size()) * 6.0f * px_x;
}

